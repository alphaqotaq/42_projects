int	ft_sqrt(int nb)
{
	int i;

	if (nb <= 0 || nb > 2147395600)
		return (0);
	i = 1;
	if (nb >= 1000000)
		i = 1000;
	if (nb >= 16000000)
		i = 4000;
	if (nb >= 100000000)
		i = 10000;
	if (nb >= 400000000)
		i = 20000;
	if (nb >= 900000000)
		i = 30000;
	if (nb >= 1600000000)
		i = 40000;
	while (i <= nb / 2)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
