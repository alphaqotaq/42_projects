#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *str;

	if ((size + 1) == 0 || !(str = (char *)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	ft_memset(str, '\0', size + 1);
	return (str);
}
